package com.oracle.model;


public class ResultModel {

	private String result;

	public ResultModel() {
		super();
	}
	
	public ResultModel(String result) {
		this.result = result;
	}

	public void setResult(String result) {
		this.result = result;
	}

	public String getResult() {
		return result;
	}
	
	
}
