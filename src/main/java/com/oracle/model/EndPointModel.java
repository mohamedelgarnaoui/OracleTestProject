package com.oracle.model;

public class EndPointModel {

	private String code;

	
	public EndPointModel() {
		super();
	}

	public EndPointModel(String code) {
		super();
		this.code = code;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}
	
}
